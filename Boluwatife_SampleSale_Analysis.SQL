USE sample_sales;
SELECT * FROM sales_data_sample;
-- CORE SALES AGGREGATIONS
-- (SALES): HIGH-VALUE ORDERS
-- Business Question: Which specific orders generated more than $5,000 in revenue? Sort them from highest to lowest sales value for logistics analysis.
SELECT ORDERNUMBER, PRODUCTCODE, QUANTITYORDERED, PRICEEACH, SALES, ORDERDATE
FROM sales_data_sample
WHERE SALES > 5000
ORDER BY SALES DESC;
-- EXECUTIVE REVENUE OVERVIEW
-- Business Question: What is our total cumulative revenue, our average transaction value (ATV), and the total volume of order line items processed for corporate review?
SELECT SUM(SALES) AS TOTALREVENUE,
AVG(SALES) AS ACERAGETRANSACTIONVALUE,
COUNT(ORDERNUMBER) AS TOTALLINEITEMSSOLD
FROM sales_data_sample;
-- TOP PRODUCT LINES
-- Business Question: Which product categories have generated a cumulative total of more than $500,000 in sales? Sort them by revenue dominance to check stock categories.
SELECT PRODUCTLINE, SUM(SALES) AS TOTALPRODUCTLINESALES
FROM sales_data_sample
GROUP BY PRODUCTLINE
HAVING TOTALPRODUCTLINESALES > 500000
ORDER BY TOTALPRODUCTLINESALES DESC;
-- WINDOW FUNCTIONS - HIGHEST VALUE DEALS BY CATEGORY
-- Business Question: Within each individual Product Line (e.g., Classic Cars, Motorcycles), what are the top 3 highest-value order line items we have ever processed?
WITH RankedSales AS (
    SELECT 
        PRODUCTLINE,
        ORDERNUMBER,
        CUSTOMERNAME,
        SALES,
        DENSE_RANK() OVER (PARTITION BY PRODUCTLINE ORDER BY SALES DESC) AS SalesRank
    FROM sales_data_sample
)
SELECT PRODUCTLINE, ORDERNUMBER, CUSTOMERNAME, SALES, SalesRank
FROM RankedSales
WHERE SalesRank <= 3;
-- ANNUAL REVENUE TRENDS
-- Business Question: What is the total revenue and order volume generated by the company per calendar year? 
SELECT 
    YEAR(STR_TO_DATE(ORDERDATE, '%m/%d/%Y %H:%i')) AS SalesYear,
    SUM(SALES) AS TotalAnnualRevenue,
    COUNT(DISTINCT ORDERNUMBER) AS TotalUniqueOrders
FROM sales_data_sample
GROUP BY YEAR(STR_TO_DATE(ORDERDATE, '%m/%d/%Y %H:%i'))
ORDER BY SalesYear ASC;
-- TOP 5 CUSTOMERS
-- Business Question: Who are our top 5 most valuable corporate clients based on total sales volume? Provide their name, city, and country for executive account management.
SELECT CUSTOMERNAME, CITY, COUNTRY,SUM(SALES) AS CUMMULATIVESPEND, COUNT(DISTINCT ORDERNUMBER) AS TOTALORDERSPLACED
FROM sales_data_sample
GROUP BY  CUSTOMERNAME, CITY, COUNTRY
ORDER BY CUMMULATIVESPEND DESC
LIMIT 5;
-- MONTHLY REVENUE PATTERNS & SEASONALITY
-- Business Question: Let's look at monthly performance across all years to identify seasonal peaks. Which months traditionally drive the highest sales traffic?
SELECT 
    YEAR(STR_TO_DATE(ORDERDATE, '%m/%d/%Y %H:%i')) AS SalesYear,
    MONTH(STR_TO_DATE(ORDERDATE, '%m/%d/%Y %H:%i')) AS MonthNumber,
    DATE_FORMAT(STR_TO_DATE(ORDERDATE, '%m/%d/%Y %H:%i'), '%M') AS MonthName,
    SUM(SALES) AS MonthlyRevenue
FROM sales_data_sample
GROUP BY SalesYear, MonthNumber, MonthName
ORDER BY SalesYear ASC, MonthNumber ASC;
